                    <h1>What is Geospatial Science?</h1>
                    <hr>
                    <h4>
                        <strong>Geospatial Science is a 21st Century spin on a very old subject - Geography.</strong>
                        <br />
                        <br />
                        Geospatial can be defined as <i>"of or relating to the relative position of things on the Earth's surface."</i>&nbsp;&nbsp;
                        Science is of course <i>"the intellectual and practical activity encompassing the systematic study of the structure and
                        behavior of the physical and natural world through observation and experiment."</i>
                    </h4>
                    <br />
                    <h3><u>Definition</u></h3>
                    <h4>
                        Therefore, Geospatial Science can be considered <strong><i>the study of the relationships between structure, behavior, and 
                        position of physical and natural elements on Earth through observation and experimentation.</i></strong>
					</h4>
                    <br />
                    <h3><u>Role of Information Technology</u></h3>
                    <h4>
                        Information technology is inextricably linked to the field of Geospatial Science. Storage, retrieval, analysis, and delivery of
                        data are key tools that make the study of relationships between physical and natural elements possible at local, regional, and 
                        global scales. <br />As web technologies have continued to rapidly evolve, cloud and high performance computing have increased 
                        storage, retrieval, and analysis capabilities, while web mapping applications have similarly enhanced data delivery mechanisms.
                    </h4>
                    <br />
                    <h3><u>Themes of Geography</u></h3>
                    <h4>
